    private void table_modifier()
            {
        int c;
        try {
            Connection con = ConnexionMysql.getCon();
            insert = con.prepareStatement("SELECT a.N_approvision AS idApp, f.nom_prenom AS nomFournisseur, p.designation AS designation, a.qteentree AS quantite FROM approvisionnement a JOIN fornisseur f ON a.N_forn = f.N_forn JOIN produit p ON a.N_prod = p.N_prod");
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();

            DefaultTableModel Df = (DefaultTableModel) jTableApprovisionnement.getModel();
            Df.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int a = 1; a < c; a++) {
                    v2.add(rs.getString("idApp"));
                    v2.add(rs.getString("nomFournisseur"));
                    v2.add(rs.getString("designation"));
                    v2.add(rs.getString("quantite"));
                }

                Df.addRow(v2);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur!");
        }

    }
    
