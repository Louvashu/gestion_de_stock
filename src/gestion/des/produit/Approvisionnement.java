    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
// TODO add your handling code here:
        String combo_fourn = jComboBox_fournisseur.getSelectedItem().toString();
        String combo_produit = jComboBox_produit.getSelectedItem().toString();
        int quantite = Integer.parseInt(jSpinnerQuantite.getValue().toString());
        try {
            if (combo_fourn.equals("Choisir") || combo_produit.equals("Choisir")) {
                JOptionPane.showMessageDialog(null, "Sélectionner le fournisseur et le produit !");
            } else {
//maka ny an'ny produit
                Connection con = ConnexionMysql.getCon();
                search = con.prepareStatement("SELECT N_prod FROM produit WHERE designation = '" + combo_produit + "'");
                ResultSet rs = search.executeQuery();
                if (rs.next()) {
                    int idProduit = rs.getInt("N_prod");
//maka ny an'ny fournisseur
                    reqFourn = con.prepareStatement("SELECT N_forn FROM fornisseur WHERE nom_prenom = '" + combo_fourn + "'");
                    ResultSet rscli = reqFourn.executeQuery();
                    if (rscli.next()) {
                        int idFourn = rscli.getInt("N_forn");

                                insert = con.prepareStatement("INSERT INTO `approvisionnement` (`N_forn`, `N_prod`, `qteentree`) VALUES(?,?,?)");
                                insert.setInt(1, idFourn);
                                insert.setInt(2, idProduit);
                                insert.setInt(3, quantite);
                                insert.executeUpdate();
                                JOptionPane.showMessageDialog(null, "Approvisionnement ajoutés avec succès!");
                                table_modifier();

                                jComboBox_fournisseur.setSelectedItem("Choisir");
                                jComboBox_produit.setSelectedItem("Choisir");
                                jSpinnerQuantite.setValue(0);
                                jSpinnerQuantite.requestFocus();
                                table_modifier();
                            
                        
                    }

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur!");
        }
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        DefaultTableModel Df = (DefaultTableModel) jTableApprovisionnement.getModel();
        int selectedIndex = jTableApprovisionnement.getSelectedRow();
        String combo_fourn = jComboBox_fournisseur.getSelectedItem().toString();
        String combo_produit = jComboBox_produit.getSelectedItem().toString();
        int quantite = Integer.parseInt(jSpinnerQuantite.getValue().toString());
        try {
            int id = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());
            if (combo_fourn.equals("Choisir") || combo_produit.equals("Choisir")) {
                JOptionPane.showMessageDialog(null, "Sélectionner le fournisseur et le produit !");
            } else {
//maka ny an'ny produit
                Connection con = ConnexionMysql.getCon();
                search = con.prepareStatement("SELECT N_prod FROM produit WHERE designation = '" + combo_produit + "'");
                ResultSet rs = search.executeQuery();
                if (rs.next()) {
                    int idProduit = rs.getInt("N_prod");
//maka ny an'ny fournisseur
                    reqFourn = con.prepareStatement("SELECT N_forn FROM fornisseur WHERE nom_prenom = '" + combo_fourn + "'");
                    ResultSet rscli = reqFourn.executeQuery();
                    if (rscli.next()) {
                        int idFourn = rscli.getInt("N_forn");
                        
                                insert = con.prepareStatement("UPDATE approvisionnement SET N_forn = ?, N_prod= ?, qteentree= ? WHERE N_approvision=?");
                                insert.setInt(1, idFourn);
                                insert.setInt(2, idProduit);
                                insert.setInt(3, quantite);
                                insert.setInt(4, id);
                                insert.executeUpdate();

                                JOptionPane.showMessageDialog(null, "Approvisionnement modifié avec succès!");
                                table_modifier();

                                jComboBox_fournisseur.setSelectedItem("Choisir");
                                jComboBox_produit.setSelectedItem("Choisir");
                                jSpinnerQuantite.setValue(0);
                                jSpinnerQuantite.requestFocus();
                                table_modifier();
                            }
                        
                    

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Veuillez verifier la date de naissance!");
        }        
    }//GEN-LAST:event_btnModifierActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel Df = (DefaultTableModel) jTableApprovisionnement.getModel();
        int selectedIndex = jTableApprovisionnement.getSelectedRow();

        try {
            int id = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());

            int dialogResult = JOptionPane.showConfirmDialog(null, "Voulez vous supprimés", "Warning", JOptionPane.YES_NO_OPTION);

            if (dialogResult == JOptionPane.YES_NO_OPTION) {
                Connection con = ConnexionMysql.getCon();

                insert = con.prepareStatement("DELETE FROM approvisionnement WHERE N_approvision=?");

                insert.setInt(1, id);
            }

            insert.executeUpdate();

            JOptionPane.showMessageDialog(null, "Approvisionnement supprimé!");
            table_modifier();

            jTextFieldId.setText("");
            jComboBox_fournisseur.setSelectedItem("Choisir");
            jComboBox_produit.setSelectedItem("Choisir");
            jSpinnerQuantite.setValue(0);
            jSpinnerQuantite.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur de la suppression");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

